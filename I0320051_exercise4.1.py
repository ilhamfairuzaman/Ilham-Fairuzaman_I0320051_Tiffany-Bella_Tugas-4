x = 6
y = 4

print('x + y = ', x+y)      # Output: x + y = 10
print('x - y = ', x-y)      # Output: x - y = 2
print('x * y = ', x*y)      # Output: x * y = 24
print('x / y = ', x/y)      # Output: x / y = 1.5
print('x // y = ', x//y)    # Output: x // y = 1
print('x ** y = ', x**y)    # Output: x ** y = 1